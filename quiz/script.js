let isLookAtHint = false

// init variable
const { values, keys } = Object
let audio
let currentQuestion = ''
let answers = ''
let diff = ''
let page = 1
let score = 0
const answered = []

// for the begining
wrapper.removeChild(main)

// For timer
let time = 5 // You can change the time here
let timeInMinutes = time * 60
let interval = null

/**
 * To start the timer
 */
const startTimer = () => {
  const timer = document.getElementById('timer')

  const minutes = Math.floor(timeInMinutes / 60)
  let seconds = timeInMinutes % 60

  timeInMinutes--
  timer.innerText = `${minutes < 10 ? '0'+minutes : minutes}:${seconds < 10 ? '0'+seconds : seconds}`

  if (timeInMinutes < 0) {
    finishQuiz()
    clearInterval(interval)
  }
}

/**
 * Handling easy button click
 */
const handleEasyBtn = () => {
  wrapper.removeChild(difficulty)
  wrapper.append(main)
  diff = 'easy'

  runQuiz()
  interval = setInterval(startTimer, 1000)
}
/**
 * Handling med button click
 */
const handleMedBtn = () => {
  wrapper.removeChild(difficulty)
  wrapper.append(main)
  diff = 'med'

  runQuiz()
  interval = setInterval(startTimer, 1000)
}
/**
 * Handling hard button click
 */
const handleHardBtn = () => {
  wrapper.removeChild(difficulty)
  wrapper.append(main)
  diff = 'hard'

  runQuiz()
  interval = setInterval(startTimer, 1000)
}

/**
 * Handling show hint
 */
const showHint = () => {
  isLookAtHint = true
  hintWrapper.removeChild(hintBtn)
  switch(diff) {
    case 'easy': hint.innerHTML = `<b>Hint:</b> ${ezHint[currentQuestion]}`
  }
  switch(diff) {
    case 'med': hint.innerHTML = `<b>Hint:</b> ${medHint[currentQuestion]}`
  }
  switch(diff) {
    case 'hard': hint.innerHTML = `<b>Hint:</b> ${hardHint[currentQuestion]}`
  }
}

/**
 * Running quiz
 */
const runQuiz = () => {
  const question = getDifficultyQuestion(diff)
  scoreInfo.innerText = `Your score: ${score}`
  switch(diff) {
    case 'easy': {
      currentQuestion = keys(ezQuestions)[question]
    }
  }
  switch(diff) {
    case 'med': {
      currentQuestion = keys(medQuestions)[question]
    }
  }
  switch(diff) {
    case 'hard': {
      currentQuestion = keys(hardQuestions)[question]
    }
  }
}

const random = () => {
  return Math.floor(Math.random() * keys(ezQuestions).length)
}
/**
 * Handling get question by difficulty
 * @returns {number}
 */
const getDifficultyQuestion = () => {
  let idx = random()

  // Filtering is the current question has passed yet
  const filterQuestion = answered.find(index => index === idx)
  if (page > 1 && filterQuestion !== undefined) {
    idx = random()
  }
  answered.push(idx)
  console.log(answered)

  switch(diff) {
    case 'easy': {
      audio = new Audio(values(ezQuestions)[idx])
    }
  }
  switch(diff) {
    case 'med': {
      audio = new Audio(values(medQuestions)[idx])
    }
  }
  switch(diff) {
    case 'hard': {
      audio = new Audio(values(hardQuestions)[idx])
    }
  }

  return idx
}

/**
 * Handle play audio
 */
const playAudio = () => {
  audio.play()
}

/**
 * To handle on change
 * @param {Event} e
 */
const handleChange = (e) => {
  answers = e.target.value
}

// finishing quiz
const finishQuiz = () => {
  setTimeout(() => {
    location.reload()
  }, 3900)
  alert(`Good job!\nYour score is ${score}\nThankyou for playing :)`)

  switch(diff) { // Insert the score to local storage
    case 'easy':
      localStorage.setItem('ezScore', score)
      break
    case 'med':
      localStorage.setItem('medScore', score)
      break
    case 'hard':
      localStorage.setItem('hardScore', score)
      break
  }
}
/**
 * Handling submit
 * @param {Event} e
 */
const handleSubmit = (e) => {
  e.preventDefault()
  if (page < 10) {
    if (answers.toLowerCase() === currentQuestion.toLowerCase()) {
      alert('Bener cuy')
      score += 10
    } 
    else {
      alert('Salah bro')
    }

    input.value = ''

    if (isLookAtHint) { // If user took a look at hint
      hintWrapper.removeChild(hint)
      hintWrapper.append(hintBtn)
      isLookAtHint = false
    }

    page++
    runQuiz()
    console.log(page, `Hasil score: ${score}`)
  }
}

